dayin = int(input("Enter the day as a number (e.g. 1): "))
if dayin == 1:
  print("Today is Monday")
elif dayin == 2:
  print("Today is Tuesday")
elif dayin == 3:
  print("Today is Wednesday")
elif dayin == 4:
  print("Today is Thursday")
elif dayin == 5:
  print("Today is Friday")
elif dayin == 6:
  print("Today is Saturday")
elif dayin == 7:
  print("Today is Sunday")
elif dayin == 67:
  print("Today's day is in the weekend")
else:
  print("Wrong digit")